int main(void)
{

	// local variables


	// Initialization

    // sistemske ure (PLL) in PIE (prekinitvena) tabela
    InitSysCtrl();
    DINT;
    InitPieCtrl();
    InitPieVectTable();
    EINT;   // Enable Global interrupt INTM
    ERTM;   // Enable Global realtime interrupt DBGM

    TIC_init();
    TIC_init_1();








    // FIR filter offline


    for(i=0; i < SIGNAL_LENGTH; i++)
    {
        sigIn[i]=0;
        sigOut[i]=0;
    }

    /* FIR Generic Filter Initialisation    */
    firFP.order = FIR_NUM_OF_COEFF - 1;
    firFP.dbuffer_ptr = dbuffer;
    firFP.coeff_ptr = (float *)coeff;
    firFP.init(&firFP);


    for(i=0; i < SIGNAL_LENGTH; i++)
    {

        // If sentence for breakpoint
        if(i > 127)
        {
            temp1 = 1.0;
        }

        // Test signal
        if(FIR_NUM_OF_COEFF == 400)
        {
            xn = 1.0*sin(2 * PI * NUMBER_OF_PERIODS_IN_WINDOW * i/(SIGNAL_LENGTH-1)) + \
                 0.2*sin(2 * PI * 5.0 * NUMBER_OF_PERIODS_IN_WINDOW * i/(SIGNAL_LENGTH-1)); // test signal for DCT filter
        }
        else
        {
            xn = xn + 1.0; // test signal for filter with Dirac coeff
        }

        // Calling FIR filter function
        sigIn[i] = xn;
        firFP.input = xn;
        firFP.calc(&firFP);
        yn = firFP.output;
        sigOut[i] = yn;
    }





	// Program
	while(1)
	{
        // stop timer 0
        TIC_stop();

	    // reference angle generation (it is supposed to reset every second - with help of timer 0)
		ref_angle = ref_angle + 1.0 * (float)TIC_time / CPU_FREQ;
		// reference angle generation, who is changing with 50 Hz
        angle_50Hz = ref_angle + 50.0 * (float)TIC_time / CPU_FREQ;

		// start timer 0 for purpose of ref_angle syntesises
		TIC_start();

		// ref_angle limit between 0 and 1
		if(ref_angle > 1.0)
		{
			ref_angle = ref_angle - 1.0;
			num_of_s_passed = num_of_s_passed + 1;
		}
		else if(ref_angle < 0.0)
		{
			ref_angle = ref_angle + 1.0;
		}

		// calculation of minutes passed from start of the while loop
        if(num_of_s_passed > 59)
        {
            num_of_s_passed = 0;
            num_of_min_passed = num_of_min_passed + 1;
        }




	}
}
